export const sjdmBarangays = sjdmDistricts.flatMap((district) =>
  district.barangays.map((barangay) => ({
    district: district.name,
    barangay,
  }))
);

export const sjdmSchools = sjdmDistricts.flatMap((district) =>
  (district.schools || []).map((school) => ({
    ...school,
    district: district.name,
  }))
);

export function buildSjdmLocationLabel({ school, barangay, district } = {}) {
  return [school, barangay, district]
    .map((value) => String(value || "").trim())
    .filter(Boolean)
    .join(", ");
}

export function findSjdmSchool(districtName, schoolName) {
  const normalizedName = String(schoolName || "").trim().toLowerCase();
  if (!normalizedName) return null;

  const district = sjdmDistricts.find((item) => item.name === districtName);
  return (
    district?.schools?.find(
      (school) => school.name.toLowerCase() === normalizedName
    ) || null
  );
}
